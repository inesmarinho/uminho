void leMaior ()
{
  int leitura;
  int maior = 0;

  do {
    scanf ("%d",&leitura);
    maior = (leitura>maior) ? leitura : maior;
    if (leitura == 0)
      {
        printf("%d\n", maior);
      }
  } while (leitura != 0);
}

