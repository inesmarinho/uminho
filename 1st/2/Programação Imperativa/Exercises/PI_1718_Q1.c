void strnoV (char t[]){

  int l=0,e=0;

  while (t[l]!='\0') {
    if (t[l]=='a'||t[l]=='e'||t[l]=='i'||t[l]=='o'||t[l]=='u'||t[l]=='A'||t[l]=='E'||t[l]=='I'||t[l]=='O'||t[l]=='U')
      l++;
    else
      t[e++]=t[l++];
  }

  t[e]='\0';
}

