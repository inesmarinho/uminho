void strrev (char s[]) {

  int i,n,t;

  for(n=0; s[n]!='\0'; n++);

  n--;

  for(i=0;i<n;i++) {
    t = s[i];
    s[i] = s[n];
    s[n] = t;
    n--;
  }
}

