void truncW (char t[], int n){
  int i=0, a=0, contador=0;

  while (t[i]!='\0')
  {
    if (t[i]==' ') {
      t[a++]=t[i++];
      contador=0;
    } else if (contador==n) {
      i++;
    } else {
      t[a++]=t[i++];
      contador++;
    }
  }

  t[a]='\0';
}

