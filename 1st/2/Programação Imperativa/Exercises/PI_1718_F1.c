// Exercício 2

int quadrado(int n) {
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < n; j++) {
      putchar('#');
    }
    putchar('\n');
  }
  return 0;
}

// Exercício 3
int xadrez(int n) {
  for (int j = 0; j < n; j++) {
    for (int i = 0; i < n; i++) {
      if ((j + i) % 2 == 0) {
        putchar('#');
      } else {
        putchar('_');
      }
    }
    putchar('\n');
  }
  return 0;
}

int main() {
  xadrez(5);

  return 0;
}
